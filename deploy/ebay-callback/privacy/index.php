    <h1>Privacy Policy</h1>
    <p class="updated">Last updated: February 12, 2026</p>

    <p><strong>myBay</strong> is a desktop listing tool that helps sellers create eBay listings from product photos.</p>

    <h2>Information We Collect</h2>
    <p>The app collects only what is needed to create eBay listings on your behalf:</p>
    <ul>
        <li><strong>eBay Account Access</strong> — OAuth tokens to list items, manage inventory, and read your seller profile. We do not store your eBay password.</li>
        <li><strong>Product Photos</strong> — Images you take are processed locally on your device and uploaded to eBay's picture hosting service. They are not sent to any other third party.</li>
        <li><strong>OpenAI Analysis</strong> — Product images are sent to OpenAI's API for identification and pricing. OpenAI's data usage policy applies to that processing.</li>
    </ul>

    <h2>How We Use Your Information</h2>
    <ul>
        <li>Create and publish eBay listings you initiate</li>
        <li>Analyze product photos to suggest titles, descriptions, and prices</li>
        <li>Store your listing history locally on your device</li>
    </ul>

    <h2>Data Storage</h2>
    <p>All data (credentials, listings, drafts) is stored locally on your computer. Nothing is stored on external servers. eBay OAuth tokens are saved in a local configuration file on your machine.</p>

    <h2>Third-Party Services</h2>
    <ul>
        <li><strong>eBay API</strong> — For listing creation and account access</li>
        <li><strong>OpenAI API</strong> — For product image analysis</li>
    </ul>

    <h2>Data Sharing</h2>
    <p>We do not sell, rent, or share your personal information with anyone beyond the third-party services listed above, which are used solely to provide the app's functionality.</p>

    <h2>Contact</h2>
    <p>Questions about this policy? Contact us at the email associated with your eBay developer account.</p>
